package com.kosmo.activity;

public class ActivityVO {
 private int act_no;
 private String user_id;
 private String user_recent_access;
 private String user_search_keyword;
public int getAct_no() {
	return act_no;
}
public void setAct_no(int act_no) {
	this.act_no = act_no;
}
public String getUser_id() {
	return user_id;
}
public void setUser_id(String user_id) {
	this.user_id = user_id;
}
public String getUser_recent_access() {
	return user_recent_access;
}
public void setUser_recent_access(String user_recent_access) {
	this.user_recent_access = user_recent_access;
}
public String getUser_search_keyword() {
	return user_search_keyword;
}
public void setUser_search_keyword(String user_search_keyword) {
	this.user_search_keyword = user_search_keyword;
}
 
 
}
