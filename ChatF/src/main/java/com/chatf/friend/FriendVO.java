package com.chatf.friend;

public class FriendVO {

	private int friend_no;
	private String receiver_id;
	private String sender_id;
	private String friend_flag;
	public int getFriend_no() {
		return friend_no;
	}
	public void setFriend_no(int friend_no) {
		this.friend_no = friend_no;
	}
	public String getReceiver_id() {
		return receiver_id;
	}
	public void setReceiver_id(String receiver_id) {
		this.receiver_id = receiver_id;
	}
	public String getSender_id() {
		return sender_id;
	}
	public void setSender_id(String sender_id) {
		this.sender_id = sender_id;
	}
	public String getFriend_flag() {
		return friend_flag;
	}
	public void setFriend_flag(String friend_flag) {
		this.friend_flag = friend_flag;
	}
	
	
}
